#Arithmetic Operators
print("Arithmetic Operators")
x=29
y=3
print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x%y)

#Comparison Operators
print("Comparison Operators")
num1=56
num2=34
print(num1<num2)
print(num1>num2)
print(num1<=num2)
print(num1>=num2)
print(num1==num2) #equal to
print(num1!=num2)  #not equal to

#Assignment Operators
print("Assignment Operators")
